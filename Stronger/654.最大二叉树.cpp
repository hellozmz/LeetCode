/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    TreeNode* constructMaximumBinaryTree(vector<int>& nums) {
        int len = nums.size();
        return dfs(nums, 0, len - 1);
    }

    TreeNode* dfs(vector<int>& nums, int begin, int end) {
        if (begin > end) {
            return nullptr;
        } else if (begin == end) {
            return new TreeNode(nums[begin]);
        } else {
            int maxIndex = getMaxIndex(nums, begin, end);
            TreeNode* root = new TreeNode(nums[maxIndex]);
            root->left = dfs(nums, begin, maxIndex - 1);
            root->right = dfs(nums, maxIndex + 1, end);
            return root;
        }
        return nullptr;
    }

    int getMaxIndex(vector<int>& nums, int begin, int end) {
        int maxIndex = begin;
        for (int i = begin + 1; i <= end; ++i) {
            if (nums[i] > nums[maxIndex]) {
                maxIndex = i;
            }
        }
        return maxIndex;
    }
};
