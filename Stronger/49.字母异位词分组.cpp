// @lc code=start
class Solution {
public:
    vector<vector<string>> groupAnagrams(vector<string>& strs) {
        vector<vector<string>> result;
        if (strs.empty()) {
            return result;
        }

        auto arrayHash = [fn = std::hash<int>{}](const array<int, 26>& arr) -> size_t {
            return accumulate(arr.begin(), arr.end(), 0u, [&](size_t acc, int num) {
                return (acc << 1) ^ fn(num);
            });
        };

        unordered_map<array<int, 26>, vector<string>, decltype(arrayHash)> mapping(0, arrayHash);
        for (auto& s : strs) {
            array<int, 26> tmp{};
            for (auto ch : s) {
                ++tmp[ch - 'a'];
            }
            mapping[tmp].emplace_back(s);
        }
        for (auto& item : mapping) {
            result.emplace_back(item.second);
        }
        return result;
    }
};
// @lc code=end

