// @lc code=start
/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    bool hasCycle(ListNode *head) {
        ListNode* h1 = head;
        ListNode* h2 = head;
        while (h1 && h2) {
            h1 = h1->next;
            h2 = h2->next;
            if (h2) {
                h2 = h2->next;
                if (h2 == h1) {
                    return true;
                }
            } else {
                return false;
            }
        }
        return false;
    }
};
