// @lc code=start
class Solution {
public:
    int climbStairs(int n) {
        // 1 -> 1
        // 2 -> 2
        // 3 -> 3
        if (n <= 3) {
            return n;
        }
        long f0 = 1, f1 = 1, tmp;
        for (int i = 0; i < n; ++i) {
            tmp = f0;
            f0 = f1;
            f1 += tmp;
        }
        return (int) f0;
    }
};
// @lc code=end

