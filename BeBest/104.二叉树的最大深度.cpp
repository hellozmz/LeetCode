// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    int maxDepth(TreeNode* root) {
        int deep = 0;
        deep = max(root, deep);
        return deep;
    }

    int max(TreeNode* root, int deep) {
        if (root == nullptr) {
            return deep;
        }
        ++deep;
        return std::max(max(root->left, deep), max(root->right, deep));
    }
};
// @lc code=end

